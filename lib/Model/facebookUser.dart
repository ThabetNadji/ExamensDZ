class facebookUser {
  String name;
  String email;
  String picture;
  String id;

  facebookUser();

  getName() {
    return name;
  }

  setName(String n) {
    name = n;
  }

  getEmail() {
    return email;
  }

  setEmail(String e) {
    email = e;
  }

  getPicture() {
    return name;
  }

  setPicture(String p) {
    name = p;
  }

  getID() {
    return id;
  }

  setID(String i) {
    id = i;
  }
}
